create database mydb_final;

use mydb_final;


CREATE TABLE Health_Record (
    Health_RecordID INT PRIMARY KEY,
    Previous_date_of_visit DATE,
    Medical_history TEXT
);




CREATE TABLE Lab_Tests (
    TestID INT PRIMARY KEY,
    Test_name VARCHAR(255),
    Test_date DATE,
    Description TEXT,
    Results TEXT
);

CREATE TABLE Contains (
    TestID INT NOT NULL,
    Health_Record_id INT NOT NULL,
    PRIMARY KEY (TestID, Health_Record_id),
    FOREIGN KEY (TestID) REFERENCES Lab_Tests(TestID),
    FOREIGN KEY (Health_Record_id) REFERENCES Health_Record(Health_RecordID)
);


CREATE TABLE Patient_Details (
    Patient_id INT PRIMARY KEY,
    name VARCHAR(255),
    age INT,
    phone_number VARCHAR(15),
    gender VARCHAR(10)
);

CREATE TABLE Insurance (
    InsuranceID INT PRIMARY KEY,
    coverage_start_date DATE,
    provider VARCHAR(255),
    policy_no VARCHAR(50),
    max_coverage DECIMAL(10, 2),
    coverage_end_date DATE,
    patiendID INT NOT NULL,
    FOREIGN KEY (patiendID) REFERENCES Patient_Details(Patient_id)
);

CREATE TABLE Regular_Patients (
    PatientID INT NOT NULL,
    testID INT,
    Health_RecordID INT NOT NULL,
    InsuranceID INT NOT NULL, 
    PRIMARY KEY (PatientID, testID),
    FOREIGN KEY (InsuranceID) REFERENCES Insurance(InsuranceID),
    FOREIGN KEY (Health_RecordID) REFERENCES health_record(Health_RecordID),
    FOREIGN KEY (testID) REFERENCES Lab_Tests(TestID),
    FOREIGN KEY (PatientID) REFERENCES Patient_Details(Patient_id)
);


CREATE TABLE Ambulance_Services (
    Driver_details VARCHAR(255),
    licenseCodeID VARCHAR(255) PRIMARY KEY,
    current_location VARCHAR(255),
    phone_number VARCHAR(15),
    availability BOOLEAN
);

CREATE TABLE Emergency_Patients (
    PatientID INT PRIMARY KEY,
    LicenseCodeID VARCHAR(255) NOT NULL,
    FOREIGN KEY (LicenseCodeID) REFERENCES Ambulance_Services(licenseCodeID),
    FOREIGN KEY (PatientID) REFERENCES Patient_Details(Patient_id)
);


CREATE TABLE Medication (
    MedicationID INT PRIMARY KEY,
    dosage VARCHAR(255),
    name VARCHAR(255),
    instructions TEXT
);

CREATE TABLE Pharmacy_Orders (
    Order_id INT PRIMARY KEY,
    quantity INT,
    order_date DATE,
    total_price DECIMAL(10, 2),
    payment_method VARCHAR(255)
);



CREATE TABLE Med_Buys (
    MedicationID INT NOT NULL,
    PatientID INT NOT NULL,
    OrderID INT,
    PRIMARY KEY (MedicationID, PatientID),
    FOREIGN KEY (MedicationID) REFERENCES Medication(MedicationID),
    FOREIGN KEY (PatientID) REFERENCES Patient_Details(Patient_id),
    FOREIGN KEY (OrderID) REFERENCES Pharmacy_Orders(Order_id)
);



CREATE TABLE Hospital_Departments (
    DeptID INT PRIMARY KEY,
    name VARCHAR(255),
    hod VARCHAR(255),
    description TEXT,
    location VARCHAR(255)
);

CREATE TABLE Doctors (
    Doctor_id INT PRIMARY KEY,
    name VARCHAR(255),
    working_hours VARCHAR(255),
    specialization VARCHAR(255),
    working_days VARCHAR(255),
    qualification VARCHAR(255),
    phone_number VARCHAR(15),
    deptID INT NOT NULL,
    FOREIGN KEY (deptID) REFERENCES Hospital_Departments(DeptID)
);

CREATE TABLE Appointments (
    Appointment_id INT PRIMARY KEY,
    date DATE,
    timings TIME,
    issue_of_symptoms TEXT,
    doctorID INT NOT NULL,
    patientID INT NOT NULL,
    FOREIGN KEY (doctorID) REFERENCES Doctors(Doctor_id),
    FOREIGN KEY (patientID) REFERENCES Patient_Details(Patient_id)
);

CREATE TABLE Doctor_Achievements (
    Achieve_id INT PRIMARY KEY,
    awards TEXT,
    publications TEXT,
    milestones TEXT,
    hours INT,
    doctrorID INT NOT NULL,
    FOREIGN KEY (doctrorID) REFERENCES Doctors(Doctor_id)
);


CREATE TABLE DocProvidesMed (
    PatientID INT NOT NULL,
    MedicationID INT NOT NULL,
    DoctorID INT,
    PRIMARY KEY (PatientID, MedicationID),
    FOREIGN KEY (PatientID) REFERENCES Patient_Details(Patient_id),
    FOREIGN KEY (MedicationID) REFERENCES Medication(MedicationID),
    FOREIGN KEY (DoctorID) REFERENCES Doctors(Doctor_id)
);



CREATE TABLE LabTestsOrdered (
    TestID INT NOT NULL,
    DoctorID INT NOT NULL,
    PRIMARY KEY (TestID, DoctorID),
    FOREIGN KEY (TestID) REFERENCES Lab_Tests(TestID),
    FOREIGN KEY (DoctorID) REFERENCES Doctors(`Doctor_id`)
);



CREATE TABLE Payment_Details (
    PaymentID INT PRIMARY KEY,
    Invoice_details TEXT,
    amount_due DECIMAL(10, 2),
    payment_method VARCHAR(255),
    date_of_payment DATE,
    payment_status VARCHAR(50)
);


CREATE TABLE Consultation (
    PatientID INT NOT NULL,
    DoctorID INT NOT NULL,
    PaymentID INT NOT NULL,
    PRIMARY KEY (PatientID, DoctorID, PaymentID),
    FOREIGN KEY (PatientID) REFERENCES Patient_Details(Patient_id),
    FOREIGN KEY (DoctorID) REFERENCES Doctors(Doctor_id),
    FOREIGN KEY (PaymentID) REFERENCES Payment_Details(PaymentID)
);



CREATE TABLE Rooms (
    RoomID INT PRIMARY KEY,
    name VARCHAR(255),
    deptID INT NOT NULL,
    FOREIGN KEY (deptID) REFERENCES Hospital_Departments(DeptID)
);

CREATE TABLE Patients_Feedback (
    FeedbackID INT PRIMARY KEY,
    comments TEXT,
    ratings INT,
    date_of_feedback DATE,
    doctorID INT NOT NULL,
    FOREIGN KEY (doctorID) REFERENCES Doctors(Doctor_id)
);

CREATE TABLE Feedback_Provided_By (
    FeedbackID INT NOT NULL,
    PatientID INT NOT NULL,
    PRIMARY KEY (FeedbackID, PatientID),
    FOREIGN KEY (FeedbackID) REFERENCES Patients_Feedback(FeedbackID),
    FOREIGN KEY (PatientID) REFERENCES Patient_Details(Patient_id)
);

CREATE TABLE Surgery (
    surgeryID INT PRIMARY KEY,
    Procedure_Description VARCHAR(255),
    Date_and_Time DATETIME,
    Type VARCHAR(255),
    Pre_n_post_Op_notes TEXT,
    doctorID INT NOT NULL,
    patientID INT,
    FOREIGN KEY (doctorID) REFERENCES Doctors(Doctor_id),
    FOREIGN KEY (patientID) REFERENCES Patient_Details(Patient_id)
);























